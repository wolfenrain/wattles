/// {@template operator}
/// Used for querying data on the database in a typed way.
/// {@endtemplate}
enum Operator {
  /// The equals operator.
  equals,

  /// The not equals operator.
  notEquals,

  /// The greater than operator.
  greaterThan,

  /// The greater than or equal operator.
  greaterThanOrEqual,

  /// The less than operator.
  lessThan,

  /// The less than or equal operator.
  lessThanOrEqual,
}
